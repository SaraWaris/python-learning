marks = float(input("Enter your marks: "))



print("\n------Grade------")
if marks >= 90:
    print("A+")
elif marks >= 80:
    print("A") 
elif marks >= 70:  
    print("B")
elif marks >= 60:
    print("C")
elif marks >= 50:
    print("D")         
else:
    print("Grade: Fail")    